def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    return x / y

print("sum of 1 and 2 is ", add(1, 2))
print("multiplication of 5 and 2 is ", multiply(5, 2))
print("substraction of 6 and 1 is ", subtract(6, 1))
print("division of 8 and 2 is ", divide(8, 2))
